#include <stdio.h>

int main() {
	
	// 변수 선언 및 초기화
	int x = 4;
	int y = 2;
	int z;


	// 더하기 후 출력
	z = x + y;
	printf("z = x + y = %d\n", z);

	// 빼기 후 출력
	z = x - y;
	printf("z = x - y = %d\n", z);
	
	// 곱하기 후 출력
	z = x * y;
	printf("z = x * y = %d\n", z);
	
	// 나누기 후 출력
	z = x / y;
	printf("z = x / y = %d\n", z);


}